namespace Sdl.Input
{
    /// <summary>
    /// Game controller axes.
    /// </summary>
    public enum GameControllerAxis
    {
        /// <summary>
        /// Invalid axis.
        /// </summary>
        Invalid = -1,

        /// <summary>
        /// Left X axis.
        /// </summary>
        LeftX,

        /// <summary>
        /// Left Y axis.
        /// </summary>
        LeftY,

        /// <summary>
        /// Right X axis.
        /// </summary>
        RightX,

        /// <summary>
        /// Right Y axis.
        /// </summary>
        RightY,

        /// <summary>
        /// The left trigger.
        /// </summary>
        TriggerLeft,

        /// <summary>
        /// The right trigger.
        /// </summary>
        TriggerRight,

        /// <summary>
        /// The maximum value.
        /// </summary>
        Max
    }
}
