namespace Sdl.Sound
{
    /// <summary>
    /// The fading status of audio.
    /// </summary>
    public enum Fading
    {
        /// <summary>
        /// No fading is going on.
        /// </summary>
        NoFading,

        /// <summary>
        /// The audio is fading in.
        /// </summary>
        FadingIn,

        /// <summary>
        /// The audio is fading out.
        /// </summary>
        FadingOut
    }
}
