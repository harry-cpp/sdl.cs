namespace Sdl.Input
{
    /// <summary>
    /// A game controller button.
    /// </summary>
    public enum GameControllerButton
    {
        /// <summary>
        /// Invalid button.
        /// </summary>
        Invalid = -1,

        /// <summary>
        /// A button.
        /// </summary>
        A,

        /// <summary>
        /// B button.
        /// </summary>
        B,

        /// <summary>
        /// X button.
        /// </summary>
        X,

        /// <summary>
        /// Y button.
        /// </summary>
        Y,

        /// <summary>
        /// Back button.
        /// </summary>
        Back,

        /// <summary>
        /// Guide button.
        /// </summary>
        Guide,

        /// <summary>
        /// Start button.
        /// </summary>
        Start,

        /// <summary>
        /// Left stick button.
        /// </summary>
        LeftStick,

        /// <summary>
        /// Right stick button.
        /// </summary>
        RightStick,

        /// <summary>
        /// Left shoulder button.
        /// </summary>
        LeftShoulder,

        /// <summary>
        /// Right shoulder button.
        /// </summary>
        RightShoulder,

        /// <summary>
        /// Directional pad up.
        /// </summary>
        DpadUp,

        /// <summary>
        /// Directional pad down.
        /// </summary>
        DpadDown,

        /// <summary>
        /// Directional pad left.
        /// </summary>
        DpadLeft,

        /// <summary>
        /// Direction pad right.
        /// </summary>
        DpadRight,

        /// <summary>
        /// Max button.
        /// </summary>
        Max
    }
}
